public class LiLii{
    public static void main(String[] args){
        int l1 = 20;
        int l2 = 3;

        System.out.println(l1+"+"+l2+"="+(l1+l2));
        System.out.println(l1+"-"+l2+"="+(l1-l2));
        System.out.println(l1+"*"+l2+"="+(l1*l2));
        System.out.println(l1+"/"+l2+"="+(l1/l2));
    }
}